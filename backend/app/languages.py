def get_language_name(code_or_name: str | list[str] | None) -> str:
    """
    Return the full language name for a given code or name.
    """
    if not code_or_name:
        return "English"

    if isinstance(code_or_name, list):
        for entry in code_or_name:
            if entry:
                return get_language_name(entry)
        return "English"

    # specific cache fix for "auto"
    if code_or_name.lower() == "auto":
        return "Auto"

    # If it's already a known name (case-insensitive check), return the proper casing
    lower_input = code_or_name.lower()
    if lower_input in NAME_TO_CODE:
        # It's a name, find the entry to get proper casing or just return input title-cased?
        # Better to return the value from CODE_TO_NAME using the code we found
        code = NAME_TO_CODE[lower_input]
        return CODE_TO_NAME.get(code, code_or_name.title())

    # If it's a code, return the name
    if code_or_name in CODE_TO_NAME:
        return CODE_TO_NAME[code_or_name]

    # Fallback: just return the input capitalized
    return code_or_name.title()


def get_language_code(name_or_code: str) -> str:
    """
    Return the ISO code for a given name or code.
    Defaults to 'en' if not found and input doesn't look like a code.
    """
    if not name_or_code:
        return "en"

    if name_or_code.lower() == "auto":
        return "auto"

    # If it's a known code
    if name_or_code in CODE_TO_NAME:
        return name_or_code

    # If it's a known name
    lower_input = name_or_code.lower()
    if lower_input in NAME_TO_CODE:
        return NAME_TO_CODE[lower_input]

    # If input is 2 chars, assume it's a code (fallback)
    if len(name_or_code) == 2:
        return name_or_code.lower()

    # Fallback default
    return "en"


def pick_language_name(*hints: str | list[str] | None) -> str:
    """
    Return the first resolvable language name from the provided hints.
    Hints may be ISO codes, human-readable names, comma-separated strings, or lists.
    Defaults to "Auto" when no hint can be resolved.
    """
    for hint in hints:
        if not hint:
            continue

        if isinstance(hint, list):
            for entry in hint:
                if entry:
                    return get_language_name(entry)
        elif isinstance(hint, str):
            first = hint.split(",")[0].strip()
            if first:
                return get_language_name(first)

    return "Auto"
