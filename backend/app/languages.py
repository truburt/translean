def _deduplicate_languages(raw_list: list[dict[str, str]]) -> list[dict[str, str]]:
    """
    Deduplicate languages by name, preferring shortest code.
    e.g. Arabic -> ar (over ar-AE, ar-EG)
    """
    grouped: dict[str, list[str]] = {}
    for lang in raw_list:
        name = lang["name"]
        code = lang["code"]
        if name not in grouped:
            grouped[name] = []
        grouped[name].append(code)
    
    deduplicated = []
    for name, codes in grouped.items():
        # Pick shortest code; if tie, sort alphabetically to be deterministic
        best_code = min(codes, key=lambda c: (len(c), c))
        deduplicated.append({"code": best_code, "name": name})
    
    return sorted(deduplicated, key=lambda x: x["name"])


